    CAST(NULL AS Nullable(UInt16))                                                      AS prompt_version,
    CAST(NULL AS Nullable(String))                                                      AS model_id,
    CAST(NULL AS Nullable(String))                                                      AS provided_model_name,
    '{}'                                                                                AS model_parameters,
    CAST(map() AS Map(LowCardinality(String), UInt64))                                  AS provided_usage_details,
    CAST(map() AS Map(LowCardinality(String), UInt64))                                  AS usage_details,
    CAST(map() AS Map(LowCardinality(String), Decimal(18, 12)))                         AS provided_cost_details,
    CAST(map() AS Map(LowCardinality(String), Decimal(18, 12)))                         AS cost_details,
    toDecimal64(0, 12)                                                                  AS total_cost,
    toDecimal128(0, 12)                                                                 AS calculated_input_cost,
    toDecimal128(0, 12)                                                                 AS calculated_output_cost,
    CAST(NULL AS Nullable(String))                                                      AS usage_pricing_tier_id,
