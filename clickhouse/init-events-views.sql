    leftUTF8(coalesce(t.input, ''), 200)                                                AS input,
    lengthUTF8(coalesce(t.input, ''))                                                   AS input_length,
    leftUTF8(coalesce(t.output, ''), 200)                                               AS output,
    lengthUTF8(coalesce(t.output, ''))                                                  AS output_length,
    mapKeys(coalesce(t.metadata, map()))                                                AS metadata_names,
    arrayMap(v -> leftUTF8(v, 200), mapValues(coalesce(t.metadata, map())))             AS metadata_values,
    ''                                                                                  AS experiment_id,
    ''                                                                                  AS experiment_name,
    CAST([] AS Array(String))                                                           AS experiment_metadata_names,
    CAST([] AS Array(String))                                                           AS experiment_metadata_values,
    ''                                                                                  AS experiment_description,
    ''                                                                                  AS experiment_dataset_id,
    ''                                                                                  AS experiment_item_id,
    CAST(NULL AS Nullable(DateTime64(6)))                                               AS experiment_item_version,
    ''                                                                                  AS experiment_item_expected_output,
    CAST([] AS Array(String))                                                           AS experiment_item_metadata_names,
    CAST([] AS Array(String))                                                           AS experiment_item_metadata_values,
    ''                                                                                  AS experiment_item_root_span_id,
    ''                                                                                  AS source,
    ''                                                                                  AS service_name,
    ''                                                                                  AS service_version,
    ''                                                                                  AS scope_name,
    ''                                                                                  AS scope_version,
    ''                                                                                  AS telemetry_sdk_language,
    ''                                                                                  AS telemetry_sdk_name,
    ''                                                                                  AS telemetry_sdk_version,
    ''                                                                                  AS blob_storage_file_path,
    toUInt64(0)                                                                         AS event_bytes,
    t.created_at                                                                        AS created_at,
    t.updated_at                                                                        AS updated_at,
    t.event_ts                                                                          AS event_ts,
    t.is_deleted                                                                        AS is_deleted
FROM default.traces t FINAL
WHERE t.is_deleted = 0;


